package transformer.base;

public class Size {

    public Size(int m, int n) {
        this.m = m;
        this.n = n;
    }

    public int getM() {
        return m;
    }

    public void setM(int m) {
        this.m = m;
    }

    int m;

    public int getN() {
        return n;
    }

    public void setN(int n) {
        this.n = n;
    }

    int n;
}
